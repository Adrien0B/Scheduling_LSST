    def oneD(A):
        a = []
        for i in A:
            for j in i:
                a.append(j)
        return np.array(a)

    def medianCurve(Eta_x,Eta_y):
        A = oneD(Eta_x)
        B = oneD(Eta_y)
        a = np.array(A,dtype='int')
        U = np.unique(a)

        X = []
        Y = []
        X_err = []
        Y_err = []

        for u in U:
            I = a==u
            N = np.sum(I)
            Ai = A[I]
            Bi = B[I]
            X.append(np.mean(Ai))
            Y.append(np.mean(Bi))
            X_err.append(np.std(Ai)/np.sqrt(N))
            Y_err.append(np.std(Bi)/np.sqrt(N))

        return [X,Y,X_err,Y_err]

